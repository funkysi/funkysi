<h1>Funky Si gets baptised</h1>
<p>On Sunday 11th at 7pm Simon Foster was baptised in St Michael le Belfrey church York. Simon Foster also known as Funky Si to many of his friends, went on an alpha course in October 2008, this completely changed his view on religion, church and christianity. Before that he was a non-believer or athiest, now he is a regular church goer. To celebrate this change he has decided to affirm his faith with a full immersion baptism.</p>

